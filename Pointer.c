int main()
{
  char *atr[] = {"0a","1bc","2dick","3edison","4fgh"};

  int num = 45 , *ptr , **ptr2ptr ;
  ptr     = &num;
  ptr2ptr = &ptr;


  printf(" atr[3] : %s\n", atr[3]);
  printf(" *atr : %s\n", *atr);
  printf(" *atr+10 : %s\n", *atr+10);


  printf("**ptr2ptr : %d\n",**ptr2ptr);

}
